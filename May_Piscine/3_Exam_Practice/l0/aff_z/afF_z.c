#include <unistd.h>

int		main(int argc, char **argv)
{
	if (argc == 2)
	{
		while (*(argv[1]))
		{
			if (*(argv[1]) == 'z')
				write(1, "z", 1);
			argv[1]++;
		}
		write(1, "\n", 1);
	}
	else
		write(1, "z\n", 2);
	return (0);
}
