char	*ft_rot42(char *str)
{
	char	curr;

	curr = str[0];
	if (!curr)
		return ("");
	if (curr >= 'a' && curr <= 'z')
		str[0] = ((curr - 'a') + 16) % 26 + 'a';
	else if (curr >= 'A' && curr <= 'Z')
		str[0] = ((curr - 'A') + 16) % 26 + 'A';
	return (ft_rot42(str + 1));
}
