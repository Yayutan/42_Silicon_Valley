int		ft_str_is_alpha(char *str)
{
	char	i;
	int		ck;

	i = str[0];
	if (!str[0])
		return (1);
	ck = (i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z');
	return (ck && ft_str_is_alpha(str + 1));
}
