int		ft_strcmp(char *s1, char *s2)
{
	if (s1[0] - s2[0] != 0)
		return (s1[0] - s2[0]);
	else if (!s1[0] && !s2[0])
		return (0);
	else
		return (ft_strcmp(s1 + 1, s2 + 1));
}
